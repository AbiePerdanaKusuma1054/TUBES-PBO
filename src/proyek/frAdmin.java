            String sql;
            sql = "INSERT INTO Data VALUES ('"+txtPesanan.getText()+"','"+txtDay.getSelectedItem()+"','"+txtMonth.getSelectedItem()+"','"+txtYear.getSelectedItem()
                    +"','"+txtAsal.getSelectedItem()+"','"+tujuan+"','"+txtJDewasa.getSelectedItem()+"','"+txtJAnak.getSelectedItem()+"','"+txtJBayi.getSelectedItem()+"','"+txtSubtotal.getText()+"')";
