    private void TableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableMouseClicked
        int baris = Table.rowAtPoint(evt.getPoint());
        String pesanan =Table.getValueAt(baris, 1).toString();
        txtPesanan.setText(pesanan);
        String hari =Table.getValueAt(baris, 2).toString();
        txtDay.setSelectedItem(hari);
        String bulan = Table.getValueAt(baris,3).toString();
        txtMonth.setSelectedItem(bulan);
        String tahun=Table.getValueAt(baris, 4).toString();
        txtYear.setSelectedItem(tahun);
        String dari = Table.getValueAt(baris, 5).toString();
        txtAsal.setSelectedItem(dari);
        String ke = Table.getValueAt(baris, 6).toString();
        txtTujuan.setSelectedItem(ke);
        String d = Table.getValueAt(baris, 7).toString();
        txtTujuan.setSelectedItem(d);
        String a = Table.getValueAt(baris, 8).toString();
        txtTujuan.setSelectedItem(a);
        String b = Table.getValueAt(baris, 9).toString();
        txtTujuan.setSelectedItem(b);
        String total =Table.getValueAt(baris, 10).toString();
        txtSubtotal.setText(total);
    }//GEN-LAST:event_TableMouseClicked

